public class Vergleichsoperatoren 
{
    public static void main(String[] args) 
    {
    int zahl1 = 1;
    int zahl2 = 3;
    System.out.println("zahl1 < zahl2 : "+ (zahl1 < zahl2));        
    System.out.println("zahl1 <= zahl2 : "+ (zahl1 <= zahl2));
    System.out.println("zahl1 == zahl2 : "+ (zahl1 == zahl2));
    System.out.println("zahl1 != zahl2 : "+ (zahl1 != zahl2));
    System.out.println("zahl1 > zahl2 : "+ (zahl1 > zahl2));
    System.out.println("zahl1 >= zahl2 : "+ (zahl1 >= zahl2));
    }
}
